package im.com.slay.ui.layout;

/**
 * Defines directionality used by layout managers. Supporting both horizontal
 * and vertical flow direction is essential for mirroring languages and for
 * creating responsive animations that adapt to the player's preferences.
 */
public enum LayoutDirection {
    LEFT_TO_RIGHT,
    RIGHT_TO_LEFT,
    TOP_TO_BOTTOM,
    BOTTOM_TO_TOP
}
